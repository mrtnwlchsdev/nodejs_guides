/*
    El frontend es la parte de un sitio web con la cual el usuario puede interactuar
    El frontend se compone principalmente de HTML, CSS y JavaScript, ademas de algunos elementos estaticos como imagenes o videos
    El backend es quien se encarga del procesamiento de datos que permiten el correcto funcionamiento de un sitio web, permitiendo el intercambio de informacion entre un cliente y un servidor web

    Servidor Web
        Un servidor es un proceso que se ejecuta en un computador, el cual se encarga de escuchar peticiones para posteriormermente enviar algun tipo de respuesta
        El formato especifico de una peticion y la posterior respuesta es llamado protocolo
        El protocolo estandar utilizado para la comunicacion entre un cliente y un servidor es HTTP (Hypertext Transfer Protocol)

    Almacenamiento de datos

        El backend de las aplicaciones web modernas incluyen una o mas bases de datos
        Las bases de datos son colecciones de informacion
        Las bases de datos se dividen en 2 tipos
        
            Relaciones :: La informacion es almacenada mediante tablas
                - MySQL
                - PostgreSQL

            No relacionales :: Generalmente utilizan sistemas de almacenamiento a traves de pares clave-valor
                - MongoDB
                - Redis

        El acceso a una base de datos relacional se realiza a traves de SQL (Structured Query Language), el cual permite acceder, modificar o eliminar registros mediante consultas creadas con la sitaxis del propio lenguaje
*/